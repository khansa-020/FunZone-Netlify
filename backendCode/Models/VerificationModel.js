import mongoose from "mongoose";

const VerificationSchema = mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    isVerified: {
      type: Boolean,
      default: false,
    },
  },
  { timestamps: true }
);

const VerificationModel = mongoose.model("Verifications", VerificationSchema);
export default VerificationModel;
